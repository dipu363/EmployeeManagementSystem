    private void jupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jupdateActionPerformed
//        Update
        String update = jTexEmId.getText();

        Connection con = null;
        Statement pst = null;

        int count = 0;

        try {

            eid = jTexEmId.getText();
            ename = jTexfname.getText();
            esnaem = jTexsur.getText();
            eadd = jTexaddress.getText();
            econ = jTexconte.getText();
            eage = jTexage.getText();
            egen = jCombgender.getSelectedItem().toString();
            edep = jCombdep.getSelectedItem().toString();
            epost = jCombopost.getSelectedItem().toString();
            eshift = jComboshift.getSelectedItem().toString();
            ehdate = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
            esalary = jTexsalary.getText();

            con = DBConnection.getDBConnection();
            String sql = "update employee_info set EMP_id='" + eid + "',EMP_F_name='" + ename + "',EMP_Surname='" + esnaem + "',EMP_Address='" + eadd + "',"
                    + "EMP_Contact='" + econ + "',EMP_Age='" + eage + "',EMP_Gender='" + egen + "',EMP_Department='" + edep + "',EMP_Post='" + epost + "',EMP_Shift='" + eshift + "',EMP_H_date='" + ehdate + "',EMP_B_salary='" + esalary + "'where EMP_id='" + update + "'";
            pst = con.createStatement();
            count = pst.executeUpdate(sql);
            if (count > 0) {
                JOptionPane.showMessageDialog(null, "update successful");

            } else {

                JOptionPane.showMessageDialog(null, "update faild");

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Error");

        }
    }//GEN-LAST:event_jupdateActionPerformed

    private void jbtndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtndeleteActionPerformed
        // TODO add your handling code here:

        if (JEmptable.getSelectedRow() >= 1) {
            String delete = jTexEmId.getText();
            int p = JOptionPane.showConfirmDialog(null, "Are you sure ", "Delete", JOptionPane.YES_NO_CANCEL_OPTION);
            Connection con = null;
            Statement stm = null;

            if (p == 0) {
                String sql = "delete from employee_info where EMP_id ='" + delete + "'";
                try {
                    con = DBConnection.getDBConnection();

                    stm = con.prepareStatement(sql);
                    stm.execute(sql);
                    JOptionPane.showMessageDialog(rootPane, "Delete Successful");
                    clear();

                    if (JEmptable.getRowCount() > 0) {
                        for (int i = JEmptable.getRowCount() - 1; i > -1; i--) {
                            model.removeRow(i);
                        }
                    }
                    int row = JEmptable.getRowCount();
                    String a = String.valueOf(row);
                    jtxtTotal.setText(a);
                } catch (Exception e) {
                    e.printStackTrace();
                } finally {
                    try {
                        con.close();
                        stm.close();
                    } catch (SQLException se) {
                        System.out.println(se);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "There ara no row selected");
        }
    }//GEN-LAST:event_jbtndeleteActionPerformed

