    private void jTexsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTexsearchKeyReleased
        // TODO add your handling code here:
        String search = jTexsearch.getText();
        if (JEmptable.getRowCount() > 0) {
            for (int i = JEmptable.getRowCount() - 1; i > -1; i--) {
                model.removeRow(i);
            }
        }
        Connection con = null;
        Statement stmt = null;
        ResultSet rs = null;
        try {
            con = DBConnection.getDBConnection();
            //Create the Statement object
            stmt = con.createStatement();

            //Execute the query
            rs = stmt.executeQuery("select * from Employee_Info where EMP_F_name='" + search + "'");

            while (rs.next()) {

                model.addRow(new Object[]{rs.getString("EMP_NO"), rs.getString("EMP_id"), rs.getString("EMP_F_name"),
                    rs.getString("EMP_Surname"), rs.getString("EMP_Address"), rs.getString("EMP_Contact"),
                    rs.getString("EMP_Age"), rs.getString("EMP_Gender"), rs.getString("EMP_Department"),
                    rs.getString("EMP_Post"), rs.getString("EMP_Shift"), rs.getString("EMP_H_date"), rs.getString("EMP_B_salary"), rs.getString("EMP_Image")});

            }
            int row = JEmptable.getRowCount();
            String a = String.valueOf(row);
            jtxtTotal.setText(a);

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                //Close the connection and statement object
                con.close();
                stmt.close();
            } catch (SQLException ex) {
                System.out.println(ex);

            }
        }
    }//GEN-LAST:event_jTexsearchKeyReleased

